defmodule SelectoMix.RawPersistence do
  @moduledoc false

  def parse_adapter(nil), do: {:ok, :ecto}
  def parse_adapter(""), do: {:ok, :ecto}
  def parse_adapter(adapter) when is_atom(adapter), do: parse_adapter(Atom.to_string(adapter))

  def parse_adapter(adapter) when is_binary(adapter) do
    case String.downcase(String.trim(adapter)) do
      "" -> {:ok, :ecto}
      "postgres" -> {:ok, :postgresql}
      "postgresql" -> {:ok, :postgresql}
      "sqlite" -> {:ok, :sqlite}
      other -> {:error, "unsupported raw persistence adapter #{inspect(other)}"}
    end
  end

  def raw_mode?(:ecto), do: false
  def raw_mode?(_mode), do: true

  def adapter_module(:postgresql), do: SelectoDBPostgreSQL.Adapter
  def adapter_module(:sqlite), do: SelectoDBSQLite.Adapter

  defp current_timestamp_sql(:postgresql), do: "NOW()"
  defp current_timestamp_sql(:sqlite), do: "CURRENT_TIMESTAMP"

  def connection_name(app_name, opts) do
    opts[:connection_name] || "#{app_name}.Database"
  end

  def saved_views_sql(config) do
    case config.adapter_mode do
      :postgresql ->
        """
        -- SavedViews table for SelectoComponents
        -- Generated by mix selecto.gen.saved_views --adapter postgresql

        CREATE TABLE IF NOT EXISTS #{config.table_name} (
          id BIGSERIAL PRIMARY KEY,
          name VARCHAR(255) NOT NULL,
          context VARCHAR(255) NOT NULL,
          params JSONB NOT NULL DEFAULT '{}',
          inserted_at TIMESTAMP NOT NULL DEFAULT NOW(),
          updated_at TIMESTAMP NOT NULL DEFAULT NOW()
        );

        CREATE UNIQUE INDEX IF NOT EXISTS idx_#{config.table_name}_name_context
          ON #{config.table_name} (name, context);
        """

      :sqlite ->
        """
        -- SavedViews table for SelectoComponents
        -- Generated by mix selecto.gen.saved_views --adapter sqlite

        CREATE TABLE IF NOT EXISTS #{config.table_name} (
          id INTEGER PRIMARY KEY AUTOINCREMENT,
          name TEXT NOT NULL,
          context TEXT NOT NULL,
          params TEXT NOT NULL DEFAULT '{}',
          inserted_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
          updated_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
        );

        CREATE UNIQUE INDEX IF NOT EXISTS idx_#{config.table_name}_name_context
          ON #{config.table_name} (name, context);
        """
    end
  end

  def saved_views_context(config) do
    p1 = placeholder(config.adapter_mode, 1)
    p2 = placeholder(config.adapter_mode, 2)
    p3 = placeholder(config.adapter_mode, 3)
    adapter_module = inspect(adapter_module(config.adapter_mode))

    """
    defmodule #{config.context_module} do
      @moduledoc \"\"\"
      Context module implementing SelectoComponents.SavedViews behaviour.

      Uses #{config.adapter_mode} persistence via #{adapter_module}.

      Generated by mix selecto.gen.saved_views --adapter #{config.adapter_mode}
      \"\"\"

      defmacro __using__(_opts \\ []) do
        conn_name = #{inspect(config.connection_name)}
        table = #{inspect(config.table_name)}
        adapter_module = #{adapter_module}
        adapter_name = #{inspect(config.adapter_mode)}

        quote bind_quoted: [
                conn_name: conn_name,
                table: table,
                adapter_module: adapter_module,
                adapter_name: adapter_name
              ] do
          @behaviour SelectoComponents.SavedViews

          @saved_views_conn conn_name
          @saved_views_table table
          @saved_views_adapter adapter_module
          @saved_views_adapter_name adapter_name

          def get_view(name, context) do
            context = scoped_context(context)

            query =
              "SELECT id, name, context, params, inserted_at, updated_at FROM \#{@saved_views_table} WHERE context = #{p1} AND name = #{p2}"

            case execute(query, [context, name]) do
              {:ok, %{rows: [row], columns: cols}} -> row_to_map(cols, row)
              {:ok, %{rows: []}} -> nil
              {:error, _} -> nil
            end
          end

          def save_view(name, context, params) do
            context = scoped_context(context)
            encoded_params = Jason.encode!(params)

            case get_view(name, context) do
              nil ->
                query =
                  "INSERT INTO \#{@saved_views_table} (name, context, params, inserted_at, updated_at) VALUES (#{p1}, #{p2}, #{p3}, #{current_timestamp_sql(config.adapter_mode)}, #{current_timestamp_sql(config.adapter_mode)})"

                case execute(query, [name, context, encoded_params]) do
                  {:ok, _} -> get_view(name, context)
                  {:error, error} -> {:error, error}
                end

              _existing ->
                query =
                  "UPDATE \#{@saved_views_table} SET params = #{p3}, updated_at = #{current_timestamp_sql(config.adapter_mode)} WHERE name = #{p1} AND context = #{p2}"

                case execute(query, [name, context, encoded_params]) do
                  {:ok, _} -> get_view(name, context)
                  {:error, error} -> {:error, error}
                end
            end
          end

          def get_view_names(context) do
            context = scoped_context(context)
            query = "SELECT name FROM \#{@saved_views_table} WHERE context = #{p1} ORDER BY name"

            case execute(query, [context]) do
              {:ok, %{rows: rows}} -> Enum.map(rows, fn [name] -> name end)
              {:error, _} -> []
            end
          end

          def list_views(context) do
            context = scoped_context(context)

            query =
              "SELECT id, name, context, params, inserted_at, updated_at FROM \#{@saved_views_table} WHERE context = #{p1} ORDER BY updated_at DESC, name ASC"

            case execute(query, [context]) do
              {:ok, %{rows: rows, columns: cols}} -> Enum.map(rows, &row_to_map(cols, &1))
              {:error, _} -> []
            end
          end

          def delete_view(name, context) do
            context = scoped_context(context)

            case get_view(name, context) do
              nil -> {:error, :not_found}
              view ->
                query = "DELETE FROM \#{@saved_views_table} WHERE name = #{p1} AND context = #{p2}"

                case execute(query, [name, context]) do
                  {:ok, _} -> {:ok, view}
                  {:error, error} -> {:error, error}
                end
            end
          end

          def rename_view(old_name, new_name, context) do
            context = scoped_context(context)
            trimmed_name = String.trim(new_name || "")

            cond do
              trimmed_name == "" -> {:error, :invalid_name}
              old_name == trimmed_name ->
                case get_view(old_name, context) do
                  nil -> {:error, :not_found}
                  view -> {:ok, view}
                end

              get_view(trimmed_name, context) ->
                {:error, :already_exists}

              true ->
                query = "UPDATE \#{@saved_views_table} SET name = #{p2}, updated_at = #{current_timestamp_sql(config.adapter_mode)} WHERE name = #{p1} AND context = #{p3}"

                case execute(query, [old_name, trimmed_name, context]) do
                  {:ok, _} ->
                    case get_view(trimmed_name, context) do
                      nil -> {:error, :not_found}
                      view -> {:ok, view}
                    end

                  {:error, error} -> {:error, error}
                end
            end
          end

          def decode_view(view) when is_map(view) do
            view["params"] || view[:params]
          end

          defp execute(query, params) do
            @saved_views_adapter.execute(@saved_views_conn, query, params, [])
          end

          defp row_to_map(columns, row) do
            columns
            |> Enum.zip(row)
            |> Enum.into(%{}, fn {column, value} ->
              {column, normalize_column_value(column, value)}
            end)
          end

          defp normalize_column_value("params", value), do: decode_json(value)
          defp normalize_column_value(_column, value), do: value

          defp decode_json(value) when is_map(value), do: value

          defp decode_json(value) when is_binary(value) do
            case Jason.decode(value) do
              {:ok, decoded} -> decoded
              _ -> %{}
            end
          end

          defp decode_json(_value), do: %{}

          defp scoped_context(context) do
            case context do
              %{} = context_map ->
                raw_context =
                  Map.get(context_map, :context) ||
                    Map.get(context_map, "context") ||
                    Map.get(context_map, :path) ||
                    Map.get(context_map, "path") ||
                    "default"

                tenant_context =
                  Map.get(context_map, :tenant) ||
                    Map.get(context_map, "tenant") ||
                    %{tenant_id: Map.get(context_map, :tenant_id) || Map.get(context_map, "tenant_id")}

                if Code.ensure_loaded?(SelectoComponents.Tenant) do
                  SelectoComponents.Tenant.scoped_context(raw_context, tenant_context)
                else
                  raw_context
                end

              _ ->
                context
            end
          end

          defp current_timestamp_sql(:postgresql), do: "NOW()"
          defp current_timestamp_sql(:sqlite), do: "CURRENT_TIMESTAMP"
        end
      end
    end
    """
  end

