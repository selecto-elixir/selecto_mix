  def saved_view_configs_context(config) do
    p1 = placeholder(config.adapter_mode, 1)
    p2 = placeholder(config.adapter_mode, 2)
    p3 = placeholder(config.adapter_mode, 3)
    p4 = placeholder(config.adapter_mode, 4)
    p5 = placeholder(config.adapter_mode, 5)
    p6 = placeholder(config.adapter_mode, 6)
    p7 = placeholder(config.adapter_mode, 7)
    adapter_module = inspect(adapter_module(config.adapter_mode))

    """
    defmodule #{config.context_module} do
      @moduledoc \"\"\"
      Adapter-backed context module for saved view configurations with view type separation.

      Generated by mix selecto.gen.saved_view_configs --adapter #{config.adapter_mode}
      \"\"\"

      defmacro __using__(_opts \\ []) do
        conn_name = #{inspect(config.connection_name)}
        table = #{inspect(config.table_name)}
        adapter_module = #{adapter_module}
        adapter_name = #{inspect(config.adapter_mode)}

        quote bind_quoted: [
                conn_name: conn_name,
                table: table,
                adapter_module: adapter_module,
                adapter_name: adapter_name
              ] do
          @view_config_conn conn_name
          @view_config_table table
          @view_config_adapter adapter_module
          @view_config_adapter_name adapter_name

          def get_view_config(name, context, view_type, opts \\ []) do
            user_id = Keyword.get(opts, :user_id)

            {query, params} =
              if user_id do
                {
                  "SELECT * FROM \#{@view_config_table} WHERE name = #{p1} AND context = #{p2} AND view_type = #{p3} AND (user_id = #{p4} OR is_public = #{truthy_sql_literal(config.adapter_mode)}) LIMIT 1",
                  [name, context, view_type, user_id]
                }
              else
                {
                  "SELECT * FROM \#{@view_config_table} WHERE name = #{p1} AND context = #{p2} AND view_type = #{p3} AND is_public = #{truthy_sql_literal(config.adapter_mode)} LIMIT 1",
                  [name, context, view_type]
                }
              end

            case execute(query, params) do
              {:ok, %{rows: [row], columns: cols}} -> row_to_map(cols, row)
              {:ok, %{rows: []}} -> nil
              {:error, _} -> nil
            end
          end

          def save_view_config(name, context, view_type, params, opts \\ []) do
            user_id = Keyword.get(opts, :user_id)
            description = Keyword.get(opts, :description)
            is_public = persist_boolean(Keyword.get(opts, :is_public, false))
            json_params = Jason.encode!(params)

            case get_view_config(name, context, view_type, user_id: user_id) do
              nil ->
                query =
                  "INSERT INTO \#{@view_config_table} (name, context, view_type, params, user_id, description, is_public, version, inserted_at, updated_at) VALUES (#{p1}, #{p2}, #{p3}, #{p4}, #{p5}, #{p6}, #{p7}, 1, #{current_timestamp_sql(config.adapter_mode)}, #{current_timestamp_sql(config.adapter_mode)})"

                case execute(query, [name, context, view_type, json_params, user_id, description, is_public]) do
                  {:ok, _} -> {:ok, get_view_config(name, context, view_type, user_id: user_id)}
                  {:error, error} -> {:error, error}
                end

              %{"user_id" => existing_user_id} = existing ->
                update_view_config(name, context, view_type, params,
                  user_id: existing_user_id || user_id,
                  description: description,
                  is_public: Keyword.get(opts, :is_public, false),
                  existing: existing
                )
            end
          end

          def list_view_configs(context, view_type, opts \\ []) do
            user_id = Keyword.get(opts, :user_id)
            include_public = Keyword.get(opts, :include_public, true)

            {query, params} =
              cond do
                user_id && include_public ->
                  {
                    "SELECT * FROM \#{@view_config_table} WHERE context = #{p1} AND view_type = #{p2} AND (user_id = #{p3} OR is_public = #{truthy_sql_literal(config.adapter_mode)}) ORDER BY updated_at DESC",
                    [context, view_type, user_id]
                  }

                user_id ->
                  {
                    "SELECT * FROM \#{@view_config_table} WHERE context = #{p1} AND view_type = #{p2} AND user_id = #{p3} ORDER BY updated_at DESC",
                    [context, view_type, user_id]
                  }

                include_public ->
                  {
                    "SELECT * FROM \#{@view_config_table} WHERE context = #{p1} AND view_type = #{p2} AND is_public = #{truthy_sql_literal(config.adapter_mode)} ORDER BY updated_at DESC",
                    [context, view_type]
                  }

                true ->
                  {"SELECT * FROM \#{@view_config_table} WHERE 1 = 0", []}
              end

            case execute(query, params) do
              {:ok, %{rows: rows, columns: cols}} -> Enum.map(rows, &row_to_map(cols, &1))
              {:error, _} -> []
            end
          end

          def delete_view_config(name, context, view_type, opts \\ []) do
            user_id = Keyword.get(opts, :user_id)

            case get_view_config(name, context, view_type, user_id: user_id) do
              nil -> {:error, :not_found}
              %{"user_id" => ^user_id} = config_map ->
                query = "DELETE FROM \#{@view_config_table} WHERE id = #{p1}"

                case execute(query, [config_map["id"]]) do
                  {:ok, _} -> {:ok, config_map}
                  {:error, error} -> {:error, error}
                end

              _ -> {:error, :unauthorized}
            end
          end

          def update_view_config(name, context, view_type, params, opts \\ []) do
            user_id = Keyword.get(opts, :user_id)
            description = Keyword.get(opts, :description)
            is_public = Keyword.get(opts, :is_public)
            existing = Keyword.get(opts, :existing)

            config_map = existing || get_view_config(name, context, view_type, user_id: user_id)

            case config_map do
              nil ->
                {:error, :not_found}

              %{"user_id" => ^user_id} = config_map ->
                json_params = Jason.encode!(params)

                query =
                  "UPDATE \#{@view_config_table} SET params = #{p2}, description = COALESCE(#{p3}, description), is_public = COALESCE(#{p4}, is_public), version = version + 1, updated_at = #{current_timestamp_sql(config.adapter_mode)} WHERE id = #{p1}"

                public_param = if is_nil(is_public), do: nil, else: persist_boolean(is_public)

                case execute(query, [config_map["id"], json_params, description, public_param]) do
                  {:ok, _} -> {:ok, get_view_config(name, context, view_type, user_id: user_id)}
                  {:error, error} -> {:error, error}
                end

              _ ->
                {:error, :unauthorized}
            end
          end

          def get_view_config_names(context, view_type, opts \\ []) do
            list_view_configs(context, view_type, opts)
            |> Enum.map(&(&1["name"] || &1[:name]))
          end

          def decode_view_config(view_config) when is_map(view_config) do
            view_config["params"] || view_config[:params]
          end

          defp execute(query, params) do
            @view_config_adapter.execute(@view_config_conn, query, params, [])
          end

          defp row_to_map(columns, row) do
            columns
            |> Enum.zip(row)
            |> Enum.into(%{}, fn {column, value} ->
              {column, normalize_column_value(column, value)}
            end)
          end

          defp normalize_column_value("params", value), do: decode_json(value)
          defp normalize_column_value("is_public", value), do: normalize_boolean(value)
          defp normalize_column_value(_column, value), do: value

          defp decode_json(value) when is_map(value), do: value

          defp decode_json(value) when is_binary(value) do
            case Jason.decode(value) do
              {:ok, decoded} -> decoded
              _ -> %{}
            end
          end

          defp decode_json(_value), do: %{}

          defp normalize_boolean(value) when value in [true, 1, "1"], do: true
          defp normalize_boolean(value) when value in [false, 0, "0"], do: false
          defp normalize_boolean(value), do: value

          defp persist_boolean(value) when @view_config_adapter_name == :sqlite do
            if value, do: 1, else: 0
          end

          defp persist_boolean(value), do: value

          defp current_timestamp_sql(:postgresql), do: "NOW()"
          defp current_timestamp_sql(:sqlite), do: "CURRENT_TIMESTAMP"
        end
      end
    end
    """
  end

