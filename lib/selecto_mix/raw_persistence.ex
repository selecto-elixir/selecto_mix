  def filter_sets_context(config) do
    p1 = placeholder(config.adapter_mode, 1)
    p2 = placeholder(config.adapter_mode, 2)
    p3 = placeholder(config.adapter_mode, 3)
    p4 = placeholder(config.adapter_mode, 4)
    p5 = placeholder(config.adapter_mode, 5)
    p6 = placeholder(config.adapter_mode, 6)
    p7 = placeholder(config.adapter_mode, 7)
    p8 = placeholder(config.adapter_mode, 8)
    adapter_module = inspect(adapter_module(config.adapter_mode))

    """
    defmodule #{config.context_module} do
      @moduledoc \"\"\"
      Context for managing saved filter sets.
      Implements SelectoComponents.FilterSetsBehaviour using #{config.adapter_mode} persistence.
      \"\"\"

      @behaviour SelectoComponents.FilterSetsBehaviour

      @conn #{inspect(config.connection_name)}
      @table #{inspect(config.table)}
      @adapter #{adapter_module}
      @adapter_name #{inspect(config.adapter_mode)}

      @impl true
      def list_personal_filter_sets(user_id, domain) do
        query =
          "SELECT * FROM \#{@table} WHERE user_id = #{p1} AND domain = #{p2} AND is_system = #{false_sql_literal(config.adapter_mode)} ORDER BY is_default DESC, name ASC"

        query_rows(query, [user_id, scoped_domain(domain)])
      end

      @impl true
      def list_shared_filter_sets(_user_id, domain) do
        query =
          "SELECT * FROM \#{@table} WHERE is_shared = #{truthy_sql_literal(config.adapter_mode)} AND domain = #{p1} AND is_system = #{false_sql_literal(config.adapter_mode)} ORDER BY name ASC"

        query_rows(query, [scoped_domain(domain)])
      end

      @impl true
      def list_system_filter_sets(domain) do
        query =
          "SELECT * FROM \#{@table} WHERE is_system = #{truthy_sql_literal(config.adapter_mode)} AND domain = #{p1} ORDER BY name ASC"

        query_rows(query, [scoped_domain(domain)])
      end

      @impl true
      def get_filter_set(id, user_id) do
        query = "SELECT * FROM \#{@table} WHERE id = #{p1}"

        case query_rows(query, [id]) do
          [] -> {:error, :not_found}
          [%{"is_system" => true} = fs] -> {:ok, fs}
          [%{"is_shared" => true} = fs] -> {:ok, fs}
          [%{"user_id" => ^user_id} = fs] -> {:ok, fs}
          _ -> {:error, :unauthorized}
        end
      end

      @impl true
      def create_filter_set(attrs) do
        attrs = atomize_keys(attrs)
        attrs = Map.put_new(attrs, :id, generate_id())
        attrs = Map.update(attrs, :domain, nil, &scoped_domain/1)
        maybe_unset_defaults(attrs)

        query =
          "INSERT INTO \#{@table} (id, name, description, domain, filters, user_id, is_default, is_shared, is_system, usage_count, inserted_at, updated_at) VALUES (#{p1}, #{p2}, #{p3}, #{p4}, #{p5}, #{p6}, #{p7}, #{p8}, ?, 0, #{current_timestamp_sql(config.adapter_mode)}, #{current_timestamp_sql(config.adapter_mode)})"

        params = [
          attrs[:id],
          attrs[:name],
          attrs[:description],
          attrs[:domain],
          Jason.encode!(attrs[:filters] || %{}),
          attrs[:user_id],
          persist_boolean(attrs[:is_default] || false),
          persist_boolean(attrs[:is_shared] || false),
          persist_boolean(attrs[:is_system] || false)
        ]

        case execute(query, params) do
          {:ok, _} -> get_filter_set(attrs[:id], attrs[:user_id])
          {:error, error} -> {:error, error}
        end
      end

      @impl true
      def update_filter_set(id, attrs, user_id) do
        attrs = atomize_keys(attrs)

        with {:ok, fs} <- get_filter_set(id, user_id),
             false <- fs["is_system"] do
          merged = Map.merge(fs, stringify_keys(attrs))
          maybe_unset_defaults(atomize_keys(merged))

          query =
            "UPDATE \#{@table} SET name = COALESCE(#{p2}, name), description = COALESCE(#{p3}, description), filters = COALESCE(#{p4}, filters), is_default = COALESCE(#{p5}, is_default), is_shared = COALESCE(#{p6}, is_shared), updated_at = #{current_timestamp_sql(config.adapter_mode)} WHERE id = #{p1}"

          params = [
            id,
            attrs[:name],
            attrs[:description],
            if(attrs[:filters], do: Jason.encode!(attrs[:filters]), else: nil),
            persist_boolean_or_nil(attrs[:is_default]),
            persist_boolean_or_nil(attrs[:is_shared])
          ]

          case execute(query, params) do
            {:ok, _} -> get_filter_set(id, user_id)
            {:error, error} -> {:error, error}
          end
        else
          true -> {:error, :cannot_modify_system}
          error -> error
        end
      end

      @impl true
      def delete_filter_set(id, user_id) do
        with {:ok, fs} <- get_filter_set(id, user_id),
             false <- fs["is_system"],
             true <- fs["user_id"] == user_id do
          case execute("DELETE FROM \#{@table} WHERE id = #{p1}", [id]) do
            {:ok, _} -> {:ok, fs}
            {:error, error} -> {:error, error}
          end
        else
          true -> {:error, :cannot_delete_system}
          false -> {:error, :unauthorized}
          error -> error
        end
      end

      @impl true
      def set_default_filter_set(id, user_id) do
        with {:ok, fs} <- get_filter_set(id, user_id) do
          _ = execute(
            "UPDATE \#{@table} SET is_default = #{false_sql_literal(config.adapter_mode)} WHERE user_id = #{p1} AND domain = #{p2} AND is_default = #{truthy_sql_literal(config.adapter_mode)}",
            [user_id, fs["domain"]]
          )

          case execute(
                 "UPDATE \#{@table} SET is_default = #{truthy_sql_literal(config.adapter_mode)}, updated_at = #{current_timestamp_sql(config.adapter_mode)} WHERE id = #{p1}",
                 [id]
               ) do
            {:ok, _} -> get_filter_set(id, user_id)
            {:error, error} -> {:error, error}
          end
        end
      end

      @impl true
      def get_default_filter_set(user_id, domain) do
        query =
          "SELECT * FROM \#{@table} WHERE user_id = #{p1} AND domain = #{p2} AND is_default = #{truthy_sql_literal(config.adapter_mode)}"

        case query_rows(query, [user_id, scoped_domain(domain)]) do
          [fs] -> fs
          _ -> nil
        end
      end

      @impl true
      def increment_usage_count(id) do
        _ = execute("UPDATE \#{@table} SET usage_count = usage_count + 1 WHERE id = #{p1}", [id])
        :ok
      end

      @impl true
      def duplicate_filter_set(id, new_name, user_id) do
        with {:ok, source} <- get_filter_set(id, user_id) do
          create_filter_set(%{
            name: new_name,
            description: source["description"],
            domain: source["domain"],
            filters: source["filters"],
            user_id: user_id,
            is_default: false,
            is_shared: false,
            is_system: false
          })
        end
      end

      defp execute(query, params) do
        @adapter.execute(@conn, query, params, [])
      end

      defp query_rows(query, params) do
        case execute(query, params) do
          {:ok, %{rows: rows, columns: cols}} -> Enum.map(rows, &row_to_map(cols, &1))
          {:error, _} -> []
        end
      end

      defp row_to_map(columns, row) do
        columns
        |> Enum.zip(row)
        |> Enum.into(%{}, fn {column, value} -> {column, normalize_column_value(column, value)} end)
      end

      defp normalize_column_value("filters", value) when is_binary(value) do
        case Jason.decode(value) do
          {:ok, decoded} -> decoded
          _ -> %{}
        end
      end

      defp normalize_column_value("filters", value) when is_map(value), do: value
      defp normalize_column_value(column, value) when column in ["is_default", "is_shared", "is_system"] do
        value in [true, 1, "1"]
      end
      defp normalize_column_value(_column, value), do: value

      defp atomize_keys(map) when is_map(map) do
        allowed_keys = %{
          "id" => :id,
          "name" => :name,
          "description" => :description,
          "domain" => :domain,
          "filters" => :filters,
          "user_id" => :user_id,
          "is_default" => :is_default,
          "is_shared" => :is_shared,
          "is_system" => :is_system,
          "usage_count" => :usage_count,
          "inserted_at" => :inserted_at,
          "updated_at" => :updated_at
        }

        map
        |> Enum.map(fn
          {k, v} when is_binary(k) ->
            case Map.get(allowed_keys, k) do
              nil -> nil
              allowed_key -> {allowed_key, v}
            end

          {k, v} -> {k, v}
        end)
        |> Enum.reject(&is_nil/1)
        |> Map.new()
      end

      defp stringify_keys(map) when is_map(map) do
        Enum.into(map, %{}, fn {k, v} -> {to_string(k), v} end)
      end

      defp maybe_unset_defaults(attrs) do
        if attrs[:is_default] do
          user_id = attrs[:user_id]
          domain = attrs[:domain]

          if user_id && domain do
            _ = execute(
              "UPDATE \#{@table} SET is_default = #{false_sql_literal(config.adapter_mode)} WHERE user_id = #{p1} AND domain = #{p2} AND is_default = #{truthy_sql_literal(config.adapter_mode)}",
              [user_id, domain]
            )
          end
        end
      end

      defp generate_id do
        Base.url_encode64(:crypto.strong_rand_bytes(12), padding: false)
      end

      defp persist_boolean(value) when @adapter_name == :sqlite, do: if(value, do: 1, else: 0)
      defp persist_boolean(value), do: value
      defp persist_boolean_or_nil(nil), do: nil
      defp persist_boolean_or_nil(value), do: persist_boolean(value)

      defp current_timestamp_sql(:postgresql), do: "NOW()"
      defp current_timestamp_sql(:sqlite), do: "CURRENT_TIMESTAMP"

      defp scoped_domain(domain) do
        case domain do
          %{} = domain_map ->
            raw_domain =
              Map.get(domain_map, :domain) ||
                Map.get(domain_map, "domain") ||
                Map.get(domain_map, :path) ||
                Map.get(domain_map, "path") ||
                "default"

            tenant_context =
              Map.get(domain_map, :tenant) ||
                Map.get(domain_map, "tenant") ||
                %{tenant_id: Map.get(domain_map, :tenant_id) || Map.get(domain_map, "tenant_id")}

            if Code.ensure_loaded?(SelectoComponents.Tenant) do
              SelectoComponents.Tenant.scoped_context(raw_domain, tenant_context)
            else
              raw_domain
            end

          _ ->
            domain
        end
      end
    end
    """
  end

  def compatibility_notice(adapter_mode) do
    case adapter_mode do
      :postgresql ->
        "Run: mix selecto.setup --adapter postgresql or execute the generated SQL manually"

      :sqlite ->
        "Execute the generated SQL against your SQLite database before using the context"
    end
  end

  defp placeholder(:postgresql, index), do: "$#{index}"
  defp placeholder(:sqlite, _index), do: "?"

  defp truthy_sql_literal(:postgresql), do: "true"
  defp truthy_sql_literal(:sqlite), do: "1"
  defp false_sql_literal(:postgresql), do: "false"
  defp false_sql_literal(:sqlite), do: "0"
